.onAttach <- function(libname, pkgname) {
  # Runs when attached to search() path such as by library() or require()
  if (interactive())
  {
    v = utils::packageVersion("lidR")
    packageStartupMessage("
lidR version ", v, ". Ask for help on <https://gis.stackexchange.com/>. Report bugs on <https://github.com/Jean-Romain/lidR>.")
  }
}

.onLoad <- function(libname, pkgname) {
  op <- options()
  op.lidR <- list(
    lidR.progress = TRUE,
    lidR.progress.delay = 2,
    lidR.verbose = FALSE,
    lidR.interactive = TRUE,
    lidR.debug = FALSE,
    lidR.buildVRT = TRUE
  )

  max <- R_omp_get_max_threads()
  if (max > 0) LIDRTHREADS$n <- max

  toset <- !(names(op.lidR) %in% names(op))
  if (any(toset)) options(op.lidR[toset])

  invisible()
}

.onUnload <- function(libpath) {
  library.dynam.unload("lidR", libpath)
}

